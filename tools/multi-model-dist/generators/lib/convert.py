"""原本(.claude/**)を正規化IRにパースし、ターゲット別シリアライザへ渡すコア。

設計（MAPPING.md に従う）:
- frontmatter/構造化変換は PyYAML / json / 内蔵 TOML シリアライザ（tomllib 往復検証付き・決定的出力）に委譲（bash 文字列連結はしない）。
- 走査は各セクション配下の <section>/.claude/** のみ。ルート直下 .claude/ と .claude-plugin/ は除外。
- 生成物にはセンチネルを付け、手書き（センチネル無し）の生成先は上書きしない。
- 本文の用語写像（/cmd→$mention/#name、Task、.claude/ パス）はターゲット別に適用する。
"""
from __future__ import annotations

import dataclasses
import json
import pathlib
import re
import yaml

# 生成物の1行目に入れるセンチネル（出典・手編集禁止・冪等更新の目印）
SENTINEL_PREFIX = "GENERATED by multi-model-dist — DO NOT EDIT. source:"
# JSON はコメント不可のため、このキーでセンチネルを表す（F1）
SENTINEL_KEY = "_generated"

# 走査から除外するトップレベルディレクトリ
EXCLUDE_TOP = {".claude", ".claude-plugin"}


def sentinel_line(source_rel: str, comment: str = "<!--", close: str = "-->") -> str:
    return f"{comment} {SENTINEL_PREFIX} {source_rel} {close}"


def has_sentinel(path: pathlib.Path) -> bool:
    """生成物（センチネル付き）かどうか。手書き保護の判定に使う。

    コメント可能な形式（md/toml/sh）は先頭行に埋め込む。コメント不可の JSON は
    `_generated` キーで表す（F1: JSON にセンチネルが無く再生成されない不具合の修正）。
    """
    if not path.exists():
        return False
    text = path.read_text(encoding="utf-8")
    head = text.splitlines()[:1]
    if head and SENTINEL_PREFIX in head[0]:
        return True
    if path.suffix == ".json":
        try:
            return SENTINEL_KEY in json.loads(text)
        except (json.JSONDecodeError, ValueError):
            return False
    return False


# ---------------------------------------------------------------------------
# frontmatter
# ---------------------------------------------------------------------------
_FM_RE = re.compile(r"^---\n(.*?)\n---\n?(.*)$", re.DOTALL)


def split_frontmatter(text: str):
    """(meta: dict|None, body: str) を返す。frontmatter 無しなら (None, text)。"""
    m = _FM_RE.match(text)
    if not m:
        return None, text
    meta = yaml.safe_load(m.group(1)) or {}
    return meta, m.group(2)


# ---------------------------------------------------------------------------
# IR
# ---------------------------------------------------------------------------
@dataclasses.dataclass
class SkillIR:
    section: str
    name: str
    description: str
    body: str
    manual_only: bool = False  # disable-model-invocation: true 由来
    has_frontmatter: bool = True


@dataclasses.dataclass
class AgentIR:
    section: str
    name: str
    description: str
    instructions: str  # 本文（Codex=developer_instructions / Kiro=prompt）
    model: str | None = None  # inherit は None に落とす
    tools: list[str] = dataclasses.field(default_factory=list)


# ---------------------------------------------------------------------------
# 走査
# ---------------------------------------------------------------------------
def iter_section_claude_dirs(repo_root: pathlib.Path):
    """各セクション配下の .claude ディレクトリを返す（ルート直下 .claude は除外）。"""
    for child in sorted(repo_root.iterdir()):
        if not child.is_dir() or child.name in EXCLUDE_TOP:
            continue
        claude = child / ".claude"
        if claude.is_dir():
            yield child.name, claude


def load_skill(section: str, skill_md: pathlib.Path) -> SkillIR:
    meta, body = split_frontmatter(skill_md.read_text(encoding="utf-8"))
    if meta is None:
        # frontmatter 無し（data-science 等）= T1g ガイダンス。name はディレクトリ名で補完。
        return SkillIR(section, skill_md.parent.name, "", body, has_frontmatter=False)
    return SkillIR(
        section=section,
        name=str(meta.get("name", skill_md.parent.name)),
        description=str(meta.get("description", "")).strip(),
        body=body,
        manual_only=bool(meta.get("disable-model-invocation", False)),
    )


def load_agent(section: str, agent_md: pathlib.Path) -> AgentIR:
    meta, body = split_frontmatter(agent_md.read_text(encoding="utf-8"))
    meta = meta or {}
    model = meta.get("model")
    if model in (None, "inherit"):
        model = None  # inherit/未指定は捨てて各ツール既定に任せる
    tools_raw = meta.get("tools", "")
    if isinstance(tools_raw, str):
        tools = [t.strip() for t in tools_raw.split(",") if t.strip()]
    elif isinstance(tools_raw, list):
        tools = [str(t).strip() for t in tools_raw]
    else:
        tools = []
    return AgentIR(
        section=section,
        name=str(meta.get("name", agent_md.stem)),
        description=str(meta.get("description", "")).strip(),
        instructions=body.strip(),
        model=model,
        tools=tools,
    )


def collect(repo_root: pathlib.Path):
    """(skills, agents, known_skill_names) を集める。"""
    skills, agents = [], []
    for section, claude in iter_section_claude_dirs(repo_root):
        for skill_md in sorted(claude.glob("skills/*/SKILL.md")):
            skills.append(load_skill(section, skill_md))
        for agent_md in sorted(claude.glob("agents/*.md")):
            agents.append(load_agent(section, agent_md))
    known = {s.name for s in skills}
    return skills, agents, known


# ---------------------------------------------------------------------------
# 本文用語写像（ターゲット別）
# ---------------------------------------------------------------------------
# CC のモデル指定（tier 名）→ 各ツールの model id 写像（MAPPING ③）。
# provider id は環境/バージョン依存のため、確証のある対応のみ持ち、未知の素の tier 名は
# 出力しない（壊れた model を出すより omit する）。`inherit` は呼び出し側で None 済み。
_MODEL_MAP = {
    "kiro": {"sonnet": "claude-sonnet-5", "opus": "claude-opus-4-8", "haiku": "claude-haiku-4-5"},
    "codex": {},  # Codex は自前モデル。tier→provider id の確証が無いため既定に委ねる
}


def map_model(model: str | None, target: str) -> str | None:
    if not model:
        return None
    return _MODEL_MAP.get(target, {}).get(model)  # 未知 tier は None（= 出力しない）


def map_body(body: str, target: str, known_skills: set[str]) -> str:
    """本文の CC 固有表記をターゲット表記へ写す。

    - 既知スキル名に一致する /<name> のみ置換（無関係な /foo は触らない＝過剰置換防止）。
      自己参照（name がそのスキル自身）も既知集合に含まれるので同じ規則で正しく変換される。
    - .claude/ パスをターゲットのパスへ。
    """
    out = body
    for name in sorted(known_skills, key=len, reverse=True):
        token = "$" + name if target == "codex" else "#" + name
        # 直前が ASCII 英数字/`_`/`/` のときだけ除外（パス段や英単語に紛れた /name を守る）。
        # 日本語など非 ASCII 語頭文字に密着した /name は対象に含める（F3: 取りこぼし修正）。
        out = re.sub(rf"(?<![A-Za-z0-9_/])/{re.escape(name)}\b", token, out)
    if target == "codex":
        out = out.replace(".claude/skills/", ".agents/skills/").replace(".claude/agents/", ".codex/agents/")
        out = out.replace(".claude/", ".codex/")   # 残りの .claude/（設定・図・配置先）の catch-all
    else:
        out = out.replace(".claude/skills/", ".kiro/skills/").replace(".claude/agents/", ".kiro/agents/")
        out = out.replace(".claude/", ".kiro/")
    return out


# ---------------------------------------------------------------------------
# CLAUDE.md → 平坦化（@import を depth-1 で展開）
# ---------------------------------------------------------------------------
# @import の対象は「パスらしい形」に限定（F5: @mention や装飾子の誤認を防ぐ）。
# 相対パス（./ ../）か、`/` を含むか、ドキュメント拡張子で終わるもののみ。
_IMPORT_RE = re.compile(r"^@(?:import\s+)?(\.{1,2}/\S+|\S+/\S+|\S+\.(?:md|markdown|txt))\s*$")


def expand_imports(text: str, base_dir: pathlib.Path, _depth: int = 1) -> str:
    """行頭 `@path`（または `@import path`）を相対解決して中身に展開する（depth-1）。

    AGENTS.md / steering は @import 非対応のため、平坦化して橋渡しする（codex-agents 同方針）。
    """
    out = []
    for line in text.splitlines():
        m = _IMPORT_RE.match(line.strip())
        if m and _depth > 0:
            target = (base_dir / m.group(1)).resolve()
            if target.is_file():
                out.append(f"<!-- expanded @import {m.group(1)} -->")
                out.append(expand_imports(target.read_text(encoding="utf-8"), target.parent, _depth - 1))
                continue
        out.append(line)
    return "\n".join(out)


def load_guidance_text(claude_md: pathlib.Path) -> str:
    """CLAUDE.md を平坦化したテキストを返す（frontmatter は持たない前提）。"""
    return expand_imports(claude_md.read_text(encoding="utf-8"), claude_md.parent)


# 生成後に本文へ残ってはいけない CC 固有語（ゴールデン/出力検証用）
def residual_cc_tokens(body: str, known_skills: set[str]) -> list[str]:
    found = []
    for name in known_skills:
        if re.search(rf"(?<![A-Za-z0-9_/])/{re.escape(name)}\b", body):
            found.append(f"/{name}")
    if ".claude/" in body:
        found.append(".claude/")
    return found
